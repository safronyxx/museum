package com.example.museum.service;

import com.example.museum.model.Hall;
import java.util.List;


/**
 * Интерфейс сервиса для управления залами музея.
 * <p>
 * Обеспечивает доступ к операциям CRUD и поиск залов по названию и этажу.
 */
public interface HallService {

    /**
     * Возвращает список всех залов, зарегистрированных в системе.
     *
     * @return неизменяемый список объектов {@link Hall}; может быть пустым, если залов нет
     */
    List<Hall> findAll();

    /**
     * Находит зал по его уникальному идентификатору.
     *
     * @param id идентификатор зала
     * @return объект {@link Hall}, если зал с указанным ID найден, иначе {@code null}
     */
    Hall findById(Long id);

    /**
     * Сохраняет или обновляет информацию о зале в базе данных.
     * <p>
     * Если у объекта {@code hall} установлен идентификатор, существующая запись обновляется.
     * В противном случае создаётся новая запись.
     *
     * @param hall объект зала для сохранения
     * @return сохранённый объект {@link Hall} с присвоенным ID (в случае создания)
     */
    Hall save(Hall hall);

    /**
     * Удаляет зал из системы по его уникальному идентификатору.
     * <p>
     * Если зал не существует, операция игнорируется.
     *
     * @param id идентификатор удаляемого зала
     */
    void deleteById(Long id);

    /**
     * Выполняет поиск залов по названию и этажу с поддержкой комбинированных фильтров.
     * <p>
     * - Если указаны оба параметра, поиск выполняется по обоим критериям одновременно.<br>
     * - Если указан только один параметр, поиск выполняется только по нему.<br>
     * - Если оба параметра {@code null} или пусты, возвращается полный список залов.
     * <p>
     * Поиск по названию регистронезависим и поддерживает частичное совпадение.
     *
     * @param name  фильтр по названию зала (опционально)
     * @param floor фильтр по номеру этажа (опционально)
     * @return список залов, соответствующих критериям поиска; может быть пустым
     */
    List<Hall> searchByNameAndFloor(String name, Integer floor);
}